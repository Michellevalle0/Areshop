   public void llenarTabla() {
       /**
        * Este m√©todo nos sirve para llenar la tabla de producto, aqui se obtienen las 
        * columnas y la lista de productos.
        */
        DefaultTableModel modelo = new DefaultTableModel();
        try {
            //Agregamos las columnas de la tabla
            modelo.addColumn("Nombre");
            modelo.addColumn("Marca");
            modelo.addColumn("Descripcion");
            modelo.addColumn("Cantidad");
            modelo.addColumn("Precio");
            modelo.addColumn("Total");
         
            //Obtenemos una lista con producto
            List<Producto> a = Producto.obtenerTodos();
            for (Producto p : a) {
                modelo.addRow(new Object[]{
                    p.getNombre(),
                    p.getMarca(),
                    p.getDescripcion(),
                    p.getCantidad(),
                    p.getPrecio(),
                    p.getTotal()
                 
                });
            }
            tblProducto.setModel(modelo);

        } catch (Exception e) {
            System.out.println(e);
        }     
      

   }

